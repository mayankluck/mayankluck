package com.example.adminpannel.ui.AdminData;

public class AdminData {
    public String userName,epicNo, adhaarNo, age,fatherName, gender, caste, subcaste, mobileNo, email, password, address, pin,
            image, aadhaarImg, flag;

    public AdminData() {
    }

    public AdminData(String userName, String epicNo, String adhaarNo, String age, String fatherName, String gender, String caste, String subcaste, String mobileNo, String email, String password, String address, String pin, String image, String aadhaarImg, String flag) {
        this.userName = userName;
        this.epicNo = epicNo;
        this.adhaarNo = adhaarNo;
        this.age = age;
        this.fatherName = fatherName;
        this.gender = gender;
        this.caste = caste;
        this.subcaste = subcaste;
        this.mobileNo = mobileNo;
        this.email = email;
        this.password = password;
        this.address = address;
        this.pin = pin;
        this.image = image;
        this.aadhaarImg = aadhaarImg;
        this.flag = flag;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEpicNo() {
        return epicNo;
    }

    public void setEpicNo(String epicNo) {
        this.epicNo = epicNo;
    }

    public String getAdhaarNo() {
        return adhaarNo;
    }

    public void setAdhaarNo(String adhaarNo) {
        this.adhaarNo = adhaarNo;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getFatherName() {
        return fatherName;
    }

    public void setFatherName(String fatherName) {
        this.fatherName = fatherName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getCaste() {
        return caste;
    }

    public void setCaste(String caste) {
        this.caste = caste;
    }

    public String getSubcaste() {
        return subcaste;
    }

    public void setSubcaste(String subcaste) {
        this.subcaste = subcaste;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public void setMobileNo(String mobileNo) {
        this.mobileNo = mobileNo;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getAadhaarImg() {
        return aadhaarImg;
    }

    public void setAadhaarImg(String aadhaarImg) {
        this.aadhaarImg = aadhaarImg;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
}
